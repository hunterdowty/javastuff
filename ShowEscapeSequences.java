public class ShowEscapeSequences {
    public static void main(String[] args) {
        System.out.println("This is a message with escape sequences:");
        System.out.println("New line:\nThis is on a new line.");
        System.out.println("Tab:\tThis is after a tab.");
        System.out.println("Backslash:\\ This is a backslash.");
        System.out.println("Double quote: \"This is in double quotes.\"");
    }
}